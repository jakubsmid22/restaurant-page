import mongoose from 'mongoose';

const AdressSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    street: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    postalCode: {
        type: String,
        required: true
    },
});

export default mongoose.model('Adress', AdressSchema);